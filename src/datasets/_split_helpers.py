def load_cluster_lookup(cluster_path: Union[str, Path]) -> pd.DataFrame:
    """Load a (<hash> -> cluster_id) lookup from a parquet file and returns
    the lookup dataframe as-is. Validates that the df has exactly one of the
    expected hash columns and a cluster_id column, and that the hash column
    is unique.

    The parquet is either the per-(function, threshold) file generated by
    `build_mmseqs_clusters.py` or the per-threshold `combined_cluster.parquet`.
    The hash column is alphabet-specific (`prot_hash`/`cds_dna_hash`/`ctg_dna_hash`
    for aa/nt_cds/nt_ctg); extra columns are left intact.
    """
    cluster_path = Path(cluster_path)
    if not cluster_path.exists():
        raise FileNotFoundError(f"cluster lookup not found: {cluster_path}")
    df = pd.read_parquet(cluster_path)
    if 'cluster_id' not in df.columns:
        raise ValueError(
            f"cluster lookup is missing column 'cluster_id' at {cluster_path}."
        )

    # Identify which alphabet's hash column is present (per the schema registry).
    accepted = tuple(s.hash_col for s in schema.SCHEMA.values())  # prot/cds_dna/ctg_dna
    hash_cols_present = [c for c in accepted if c in df.columns]

    if not hash_cols_present: # raise if none of the expected hash columns are present
        raise ValueError(
            f"cluster lookup is missing a hash column at {cluster_path}. "
            f"Expected exactly one of: {accepted}."
        )

    if len(hash_cols_present) > 1: # raise if multiple hash columns are present (must be exactly one)
        raise ValueError(
            f"cluster lookup has multiple hash columns at {cluster_path} "
            f"{hash_cols_present}; expected exactly one (alphabet-specific)."
        )

    hash_col = hash_cols_present[0]
    if df[hash_col].duplicated().any():
        n_dup = int(df[hash_col].duplicated().sum())
        raise ValueError(
            f"cluster lookup has {n_dup} duplicate {hash_col} rows at {cluster_path}. "
            f"Each hash must map to exactly one cluster_id."
        )
    return df


def attach_cluster_ids(
    pos_df: pd.DataFrame,
    cluster_lookup: pd.DataFrame,
    pos_hash_col: str = 'prot_hash',
    ) -> tuple[pd.DataFrame, dict]:
    """Add `cluster_id_a` and `cluster_id_b` columns to pos_df via a join
    on the chosen hash column.

    For aa cluster_disjoint (default): join `pos_df.prot_hash_{a,b}` against
    `cluster_lookup.prot_hash`. For nt_cds cluster_disjoint, pass
    `pos_hash_col='cds_dna_hash'`; the join then consumes
    `pos_df.cds_dna_hash_{a,b}` (attached via
    `attach_cds_dna_hash_to_pos_df`) and joins against
    `cluster_lookup.cds_dna_hash`. Post Phase 2: `parse_cluster_tsv`
    writes the alphabet-specific hash column name directly, so the
    join column on the lookup side matches `pos_hash_col` exactly —
    only a side-suffix rename (hash → hash_a / hash_b) is needed.

    Pairs whose pos-side hash is missing from the lookup are DROPPED.

    Returns:
        (pos_df_with_ids, audit_dict)
        pos_df_with_ids has cluster_id_a / cluster_id_b columns added.
        audit_dict has counts: n_input, n_kept, n_dropped_missing_a,
        n_dropped_missing_b, n_dropped_missing_both, and pos_hash_col.
    """
    pos_col_a = f'{pos_hash_col}_a'
    pos_col_b = f'{pos_hash_col}_b'
    if {pos_col_a, pos_col_b} - set(pos_df.columns):
        raise ValueError(
            f"pos_df must contain {pos_col_a} and {pos_col_b} columns."
        )
    if {pos_hash_col, 'cluster_id'} - set(cluster_lookup.columns):
        raise ValueError(
            f"cluster_lookup must contain {pos_hash_col!r} and cluster_id columns."
        )

    lookup = cluster_lookup[[pos_hash_col, 'cluster_id']].drop_duplicates(subset=pos_hash_col)

    # Attach cluster_id_{a,b} to pos_df
    out = pos_df.merge(
        lookup.rename(columns={pos_hash_col: pos_col_a, 'cluster_id': 'cluster_id_a'}),
        on=pos_col_a, how='left',
    )
    out = out.merge(
        lookup.rename(columns={pos_hash_col: pos_col_b, 'cluster_id': 'cluster_id_b'}),
        on=pos_col_b, how='left',
    )

    missing_a = out['cluster_id_a'].isna()
    missing_b = out['cluster_id_b'].isna()
    missing_both = missing_a & missing_b
    drop_mask = missing_a | missing_b

    audit = {
        'n_input': int(len(pos_df)),
        'n_kept': int((~drop_mask).sum()),
        'n_dropped_missing_a': int((missing_a & ~missing_both).sum()),
        'n_dropped_missing_b': int((missing_b & ~missing_both).sum()),
        'n_dropped_missing_both': int(missing_both.sum()),
        'pos_hash_col': pos_hash_col,
    }

    kept = out[~drop_mask].reset_index(drop=True)
    return kept, audit


